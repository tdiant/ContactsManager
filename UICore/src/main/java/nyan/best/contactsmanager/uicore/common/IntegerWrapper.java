package nyan.best.contactsmanager.uicore.common;

public class IntegerWrapper {

    private int value = 0;

    public IntegerWrapper() {
    }

    public IntegerWrapper(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
